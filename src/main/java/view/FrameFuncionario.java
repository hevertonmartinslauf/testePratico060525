    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        if(validarCampos()){
            
            Funcionario f = new Funcionario();
            
            Date data = null;
            
            SimpleDateFormat formatter = new SimpleDateFormat ("dd/MM/yyyy");
            
            try {
                data = formatter.parse(txtDtEmissao.getText());
            } catch (ParseException ex) {
                Logger.getLogger(FrameFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
      
            if (cmbStatus.getSelectedItem().toString().equals("V")){
                f.setStatus(true);
            } else if (cmbStatus.getSelectedItem().toString().equals("F")){
                f.setStatus(false);
            }
            
            f.setNome(txtNome.getText());
            f.setDtEmissao(data);
            f.setSalario(Float.parseFloat(txtSalario.getText()));
            
            try {
                control.cadastrarFuncionario(f);
                txtNome.setText("");
                txtDtEmissao.setText("");
                txtSalario.setText("");
                cmbStatus.setSelectedIndex(0);
                
                control.listarTodosFuncionarios(tabelaFuncionarios);
                
                JOptionPane.showMessageDialog(this, "CADASTRO REALIZADO COM SUCESSO!");
            } 
            catch (SQLException ex) {
                 JOptionPane.showMessageDialog(this, "ERRO DE BANCO DE DADOS " + ex.getMessage() );
            } 
            
            catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Classe não encontrada");
            }   
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        int linha = tabelaFuncionarios.getSelectedRow();
        
        SimpleDateFormat dataString = new SimpleDateFormat ("dd/MM/yyyy");
        
        if(linha >= 0){
            
            funcionarioSelecionado = (Funcionario)tabelaFuncionarios.getValueAt(linha, 1);
            
           if(validarCampos()){

                Date data = null;

                SimpleDateFormat formatter = new SimpleDateFormat ("dd/MM/yyyy");

                try {
                    data = formatter.parse(txtDtEmissao.getText());
                } catch (ParseException ex) {
                    Logger.getLogger(FrameFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }

                if (cmbStatus.getSelectedItem().toString().equals("V")){
                    funcionarioSelecionado.setStatus(true);
                } else if (cmbStatus.getSelectedItem().toString().equals("F")){
                    funcionarioSelecionado.setStatus(false);
                }

                funcionarioSelecionado.setNome(txtNome.getText());
                funcionarioSelecionado.setDtEmissao(data);
                funcionarioSelecionado.setSalario(Float.parseFloat(txtSalario.getText()));

                try {
                    control.alterarFuncionario(funcionarioSelecionado);
                    txtNome.setText("");
                    txtDtEmissao.setText("");
                    txtSalario.setText("");
                    cmbStatus.setSelectedIndex(0);
                    
                    control.listarTodosFuncionarios(tabelaFuncionarios);

                    JOptionPane.showMessageDialog(this, "FUNCIONARIO ALTERADO COM SUCESSO!");
                } 
                catch (SQLException ex) {
                     JOptionPane.showMessageDialog(this, "ERRO DE BANCO DE DADOS " + ex.getMessage() );
                } 

                catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Classe não encontrada");
                }   
            }
            
            }
        
            else{
                    JOptionPane.showMessageDialog(this, "Para alterar, selecione um funcionario na tabela");
            }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        
        int linha = tabelaFuncionarios.getSelectedRow();
        
        SimpleDateFormat dataString = new SimpleDateFormat ("dd/MM/yyyy");
        
        if(linha >= 0){
            
            funcionarioSelecionado = (Funcionario)tabelaFuncionarios.getValueAt(linha, 1);
            
            if ( JOptionPane.showConfirmDialog(this, "Excluir Funcionário ? " +
                        funcionarioSelecionado.getNome(), "Confirmar", JOptionPane.YES_NO_OPTION  ) == JOptionPane.OK_OPTION  ) {
            
                try {
                        control.deletarFuncionario(funcionarioSelecionado);
                        txtNome.setText("");
                        txtDtEmissao.setText("");
                        txtSalario.setText("");
                        cmbStatus.setSelectedIndex(0);

                        control.listarTodosFuncionarios(tabelaFuncionarios);

                        JOptionPane.showMessageDialog(this, "FUNCIONARIO EXCLUIDO COM SUCESSO!");
                    } 
                    catch (SQLException ex) {
                         JOptionPane.showMessageDialog(this, "ERRO DE BANCO DE DADOS " + ex.getMessage() );
                    } 

                    catch (ClassNotFoundException ex) {
                        JOptionPane.showMessageDialog(this, "Classe não encontrada");
                    } 
            
              }  
            }
            else{
                JOptionPane.showMessageDialog(this, "Para excluir, selecione um funcionario na tabela");
            }
        
    }//GEN-LAST:event_btnDeletarActionPerformed

