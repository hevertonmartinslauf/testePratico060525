    private void tabelaFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaFuncionariosMouseClicked
        
        int linha = tabelaFuncionarios.getSelectedRow();
        
        SimpleDateFormat dataString = new SimpleDateFormat ("dd/MM/yyyy");
        
        if(linha >= 0){
            funcionarioSelecionado = (Funcionario)tabelaFuncionarios.getValueAt(linha, 1);
            
            txtNome.setText(funcionarioSelecionado.getNome());
            txtSalario.setText(String.valueOf(funcionarioSelecionado.getSalario()));
            txtDtEmissao.setText(dataString.format(funcionarioSelecionado.getDtEmissao()));
            
            if(funcionarioSelecionado.getStatus()){
                cmbStatus.setSelectedIndex(1);
            } else{
                cmbStatus.setSelectedIndex(2);
            }
            
        }
        
    }//GEN-LAST:event_tabelaFuncionariosMouseClicked

