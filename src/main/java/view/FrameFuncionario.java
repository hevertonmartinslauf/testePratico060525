    public boolean validarCampos(){
        
        Util util = new Util();
        
        String nome = txtNome.getText().toLowerCase();
        String dtEmissao = txtDtEmissao.getText();
        String salario = txtSalario.getText();
        Boolean status = null;
        String validaCampos = "";
        
        if (cmbStatus.getSelectedItem().toString().equals("V")){
            status = true;
        } else if (cmbStatus.getSelectedItem().toString().equals("F")){
            status = false;
        }
       
        if(nome.isEmpty() || dtEmissao.isEmpty() || salario.isEmpty()){            
            validaCampos = validaCampos+"PREENCHA TODOS OS CAMPOS\n";
        }
        
        if (cmbStatus.getSelectedItem().toString().equals("Selecione...")){
            validaCampos = validaCampos+"SELECIONE V OU F NO STATUS\n";    
        }
      
        if (!util.dataValida(dtEmissao)){
            validaCampos = validaCampos+"DATA INVÁLIDA\n";
        }
        
        if (!util.salarioValido(salario)){
            validaCampos = validaCampos+"SALÁRIO INVÁLIDO\n";
        }
        
        if(validaCampos.isEmpty()){
            //JOptionPane.showMessageDialog(null, "TODOS OS CAMPOS VÁLIDOS");
            return true;
        }
        
        JOptionPane.showMessageDialog(null, validaCampos);
        return false;
        
    }
    
